package privilege;

import ssm.entity.BaseEntity;

/**
 * 保存键值对
 * @author liaoyun
 * 2016-4-19
 */
public class Item extends BaseEntity{

	/**
	 * 
	 */
	private static final long serialVersionUID = 3434406987683823897L;
	private String code;
	private String desc;
	public Item() {
		super();
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
}
